# 2 Timothy 3:3 — inside the vice list the gloss reads "brutal, no lovers of good,
# treacherous". "no lovers of good" is not English: in a run of predicate adjectives
# the determiner "no" cannot negate the phrase, and a reader re-parses it as a
# quantifier ("no lovers ... exist"). "not lovers of good" is the ordinary English
# negation and is what the list needs.
#
# Anchoring: the unit is ἀφιλάγαθοι, alpha-privative + φιλάγαθος ("lover of good"),
# i.e. exactly "not a lover of good". "not lovers of good" is if anything a tighter
# gloss of the alpha-privative than "no lovers of good". No reorder, no other unit
# touched, no punctuation changed (the trailing comma is kept).
