# Revelation 12:6 read "where she has there a place prepared by God". The ἐκεῖ
# is a Semitic resumptive adverb picking up ὅπου ("where ... there"); Greek uses
# it, English does not, and left standing between the verb and its object it
# breaks the clause: "she has there a place".
#
# Fold ἐκεῖ (english "") — the established convention for an untranslatable
# particle. The Greek and its tag still render; only the redundant English goes.
# "where" (ὅπου) already carries the locative sense honestly, so nothing is lost.
# The later ἐκεῖ at unit 109 ("so that there they might nourish her") is left
# alone — that one reads as natural English.
CHAPTER = "Revelation 12"
SECTION = 0
BLOCKS = [
    (103, 104, [
        (103, ""),   # ἐκεῖ — folded
    ]),
]
