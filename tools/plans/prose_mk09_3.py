# Mark 9:41 read "in my name because of Christ you are—" — Greek order; the
# predicate genitive stands ahead of its verb, and a reader takes "because of
# Christ" as a causal phrase rather than "because you belong to Christ".
# This gives "in my name because you are of Christ—".
# Pure reorder; the only gloss edits move the dash to the new final unit.
CHAPTER = "Mark 9"
SECTION = 3
BLOCKS = [
    (53, 56, [
        (53, None),             # ὅτι — because
        (55, "you are"),        # ἐστε — loses the dash
        (54, "of Christ—"),     # χριστοῦ — takes the dash
    ]),
]
