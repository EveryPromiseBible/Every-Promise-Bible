# Acts 24:23 read "...I'll decide your case.” Ordering the centurion that he be
# kept..." -- the participle clause sits after a closed quotation with a full
# stop in front of it, so it has no finite verb and reads as a fragment.
# Rendering διαταξάμενος as the finite "He ordered" (aorist middle participle,
# subject Felix, carried from Ἀνεβάλετο) completes the sentence. No other unit
# is touched and no word is added beyond the subject pronoun the participle
# already carries.
# 14 διαταξάμενος "Ordering" -> "He ordered"
CHAPTER = "Acts 24"
SECTION = 2
BLOCKS = [
    (14, 15, [
        (14, "He ordered"),   # διαταξάμενος
    ]),
]
