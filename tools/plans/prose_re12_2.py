# Revelation 12:14 read "to her place where she is nourished there for a time".
# Same defect as 12:6 — the resumptive ἐκεῖ after ὅπου ("where ... there"),
# which is Greek idiom and not English. It strands "there" between the verb and
# the time phrase.
#
# Fold ἐκεῖ (english ""), the standard treatment for an untranslatable particle.
# ὅπου already carries "where" honestly. Result: "to her place where she is
# nourished for a time, and times, and half a time".
CHAPTER = "Revelation 12"
SECTION = 2
BLOCKS = [
    (29, 30, [
        (29, ""),   # ἐκεῖ — folded
    ]),
]
