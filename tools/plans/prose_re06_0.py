# Revelation 6:2, 6:4, 6:5 all read "And its rider on it ..." — the possessive
# "its rider" already carries the horse, so the following "on it" (ἐπ’ αὐτόν)
# lands as a redundant stumble: "its rider on it held a bow".
#
# The fourth horseman in this same section (unit 122) already glosses the
# identical Greek ὁ καθήμενος as "the one who sat", followed by "on it" —
# "And the one who sat on it had the name Death". That reads cleanly and is the
# formula the section should use throughout.
#
# So this brings the first three horsemen into line with the fourth. Anchoring
# is unchanged and stays honest: ὁ καθήμενος / τῷ καθημένῳ is the substantival
# participle "the one who sat/is seated" (the same gloss already used at 122),
# and ἐπ’ αὐτόν keeps "on it". No reordering; gloss text only.
CHAPTER = "Revelation 6"
SECTION = 0
BLOCKS = [
    (25, 26, [
        (25, "the one who sat"),        # ὁ καθήμενος
    ]),
    (53, 54, [
        (53, "the one who sat"),        # τῷ καθημένῳ
    ]),
    (80, 81, [
        (80, "And the one who sat"),    # καὶ ὁ καθήμενος — keeps the leading "And"
    ]),
]
