# 1 Peter 4:6 read "so that though they were judged in the flesh as men are, but
# might live in the spirit as God does." The "though ... but" pair is not English —
# "but" strands the second clause with no subject and no finite construction, so a
# reader stops dead.
#
# This is the mu/de correlative (men ... de) glossed word-for-word. No reorder is
# needed; two glosses change, both staying honestly anchored:
#   68  de    "but" -> "yet"   — de after a preceding men is adversative; "yet" is a
#                                standard gloss for it and pairs with the "though"
#                                already sitting on men (64). "but" was not wrong as
#                                a dictionary gloss, only wrong in this slot.
#   69  zosi  "might live" -> "they might live" — zosi is 3rd person PLURAL
#                                subjunctive; the subject "they" is carried inside
#                                the verb form itself, exactly as "they were judged"
#                                already sits on krithosi (65) for the same reason.
#                                So the added pronoun is anchored to this word.
#
# Result: "...so that though they were judged in the flesh as men are, yet they
# might live in the spirit as God does."
CHAPTER = "1 Peter 4"
SECTION = 0
BLOCKS = [
    (68, 70, [
        (68, "yet"),            # δὲ
        (69, "they might live"), # ζῶσι
    ]),
]
