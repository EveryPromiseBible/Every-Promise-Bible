# John 19:32 read "and of the other the one crucified with him" — a doubled
# article (τοῦ ἄλλου τοῦ συσταυρωθέντος) left standing, so "of the other" and
# "the one" collide. Now: "and of the other who was crucified with him".
# No units move. Gloss edits:
#   48 τοῦ              "the one"        -> folded (the attributive article; folding
#                                          it is the established convention)
#   49 συσταυρωθέντος   "crucified with" -> "who was crucified with"
#      (still exactly what the aorist passive participle says — the relative
#      pronoun just carries the article's work into English)
CHAPTER = "John 19"
SECTION = 3
BLOCKS = [
    (48, 50, [
        (48, ""),                           # τοῦ
        (49, "who was crucified with"),     # συσταυρωθέντος
    ]),
]
