# Acts 20:2-3 read "...he came to Greece, and spending three months." -- a bare
# participle left as a sentence with a full stop after it, so the clause has no
# finite verb and reads as a fragment. ποιήσας is an aorist participle carried
# by ἦλθεν; rendering it as the coordinated finite "spent" makes the sentence
# complete without touching any other word.
# 24 ποιήσας "spending" -> "spent"
